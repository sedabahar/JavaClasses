package com.SADECE.benimicin;

public class Switch {

	public static void main(String[] args) {
	
		
	int day=9;
	String mon=null;
		
	switch(day)	{
	
	case 1:
		mon="Jan";
		break;
	case 2:
		mon="F";
	  break;
	case 3:
		mon="Ma";
		break;
	case 4:
		mon="Ap";
		break;
	
	default:
		mon="No";
	
	
	}System.out.println(mon);

		
		
	}

}
